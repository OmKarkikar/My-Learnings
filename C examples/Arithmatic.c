#include <stdio.h>

int main() {
    int a,b,c,d,e,f,g;
    printf("Enter value of a=");
    scanf("%d", &a);
    printf("Enter value of b=");
    scanf("%d", &b);
    c =a+b;
    d =a-b;
    e =a*b;
    f =a/d;
    g =a%b;
    printf("Value of c is %d\n",c);
    printf("Value of d is %d\n",d);
    printf("Value of e is %d\n",e);
    printf("Value of f is %d\n",f);
    printf("Value of g is %d\n",g);
    return 0;
}